//Mouse effect for carousel page
document.getElementById('project1').addEventListener('mouseover', mouseOver);
document.getElementById('project1').addEventListener('mouseout', mouseOut);

function mouseOver() {
  document.getElementById('screen1').style.visibility = 'visible';
  document.getElementById('screen1').style.transition = '0.8s';
  document.getElementById('screen1').style.opacity = '1';
}

function mouseOut() {
  document.getElementById('screen1').style.visibility = 'hidden';
  document.getElementById('screen1').style.transition = '0.8s';
  document.getElementById('screen1').style.opacity = '0';
}

